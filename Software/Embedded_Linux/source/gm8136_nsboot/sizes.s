; /* handy sizes */
SZ_1		EQU	(1 << 0)	;0x00000001
SZ_2		EQU	(1 << 1)	;0x00000002
SZ_4		EQU	(1 << 2)	;0x00000004
SZ_8		EQU	(1 << 3)	;0x00000008
SZ_16		EQU	(1 << 4)	;0x00000010
SZ_32		EQU	(1 << 5)	;0x00000020
SZ_64		EQU	(1 << 6)	;0x00000040
SZ_128		EQU	(1 << 7)	;0x00000080
SZ_256		EQU	(1 << 8)	;0x00000100
SZ_512		EQU	(1 << 9)	;0x00000200

SZ_1K		EQU	(1 << 10)	;0x00000400
SZ_2K		EQU	(1 << 11)	;0x00000800
SZ_4K		EQU	(1 << 12)	;0x00001000
SZ_8K		EQU	(1 << 13)	;0x00002000
SZ_16K		EQU	(1 << 14)	;0x00004000
SZ_32K		EQU	(1 << 15)	;0x00008000
SZ_64K		EQU	(1 << 16)	;0x00010000
SZ_128K		EQU	(1 << 17)	;0x00020000
SZ_256K		EQU	(1 << 18)	;0x00040000
SZ_512K		EQU	(1 << 19)	;0x00080000

SZ_1M		EQU	(1 << 20)	;0x00100000
SZ_2M		EQU	(1 << 21)	;0x00200000
SZ_4M		EQU	(1 << 22)	;0x00400000
SZ_8M		EQU	(1 << 23)	;0x00800000
SZ_16M		EQU	(1 << 24)	;0x01000000
SZ_32M		EQU	(1 << 25)	;0x02000000
SZ_64M		EQU	(1 << 26)	;0x04000000
SZ_128M		EQU	(1 << 27)	;0x08000000
SZ_256M		EQU	(1 << 28)	;0x10000000
SZ_512M		EQU	(1 << 29)	;0x20000000

SZ_1G		EQU	(1 << 30)	;0x40000000
SZ_2G		EQU	(1 << 31)	;0x80000000

SZ_30M		EQU	(SZ_32M - SZ_2M)



