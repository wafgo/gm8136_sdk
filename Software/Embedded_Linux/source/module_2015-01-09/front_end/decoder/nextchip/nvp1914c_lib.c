int nvp1914c_video_set_mode(int id, NVP1914C_VMODE_T mode)
{
    int ret;

    if(id >= NVP1914C_DEV_MAX || mode >= NVP1914C_VMODE_MAX)
        return -1;

    switch(mode) {
        case NVP1914C_VMODE_NTSC_720H_4CH:
            ret = nvp1914c_ntsc_720h_4ch(id);
            break;
        case NVP1914C_VMODE_NTSC_960H_4CH:
            ret = nvp1914c_ntsc_960h_4ch(id);
            break;
        case NVP1914C_VMODE_PAL_720H_4CH:
            ret = nvp1914c_pal_720h_4ch(id);
            break;
        case NVP1914C_VMODE_PAL_960H_4CH:
            ret = nvp1914c_pal_960h_4ch(id);
            break;
        default:
            ret = -1;
            printk("NVP1914C#%d driver not support video output mode(%d)!!\n", id, mode);
            break;
    }

    /* Update VMode */
    if(ret == 0)
        nvp1914c_vmode[id] = mode;

    return ret;
}
