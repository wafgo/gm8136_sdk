int nvp1914c_video_get_mode(int id)
{
    int ret;
    int is_ntsc;
    int is_960h;

    if(id >= NVP1914C_DEV_MAX)
        return -1;

    /* get ch#0 current register setting for determine device running mode */
    ret = nvp1914c_i2c_write(id, 0xFF, 0x00);
    if(ret < 0)
        goto exit;
    is_ntsc = (((nvp1914c_i2c_read(id, 0x08)>>5)&0x03) == 0x01) ? 1 : 0;

    ret = nvp1914c_i2c_write(id, 0xFF, 0x03);
    if(ret < 0)
        goto exit;
    is_960h = ((nvp1914c_i2c_read(id, 0x63)&0x01) == 0x00) ? 1 : 0;

    if(is_ntsc) {
        if(is_960h)
            ret = NVP1914C_VMODE_NTSC_960H_4CH;
        else
            ret = NVP1914C_VMODE_NTSC_720H_4CH;
    }
    else {
        if(is_960h)
            ret = NVP1914C_VMODE_PAL_960H_4CH;
        else
            ret = NVP1914C_VMODE_PAL_720H_4CH;
    }

exit:
    return ret;
}
EXPORT_SYMBOL(nvp1914c_video_get_mode);

